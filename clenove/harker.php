Narozen 6. 10. 1944 v Montgomery, Alabama<br>
Vstoupil do služby v Fort Campbell, Kentucky jako příslušník 101. výsadkové divize. Roku 1966 na vlastni žádost převelen do Vietnamu. Po 6 měsících služba předčasně ukončena kvůli zranění. Roku 1967 opět vstoupil do výcviku a absolvoval Airborne RECONDO Course v Fort Campbell. Roku 1968 opět převelen do Vietnamu jako příslušník Company F (LRP). 58th Infantry. Roku 1969 převelen k Company D/75th Infantry Ranger.<br>
<br>Současné přidělení u k Company D/75th Infantry Ranger - 2nd Field Force Vietnam.

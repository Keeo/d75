<div class=treti>
Narozen 15. 3. 1947 v Nha Trang, South Vietnam.<br>
Povolán do služby v Nha Trang, South Vietnam.<br>
Povolán do služby roku 1964 a zařazen jako příslušník ARVN. Díky svojí znalosti angličtiny roku 1966 nastoupil do BDQ (Biet Dông Quân) výcviku, pod vedením amerických instruktorů z řad Rangers. Roku 1967 dokončil výcvik a byl přidělen k 1st Brigade LRRP 101. vysadkové divize jako tlumočník a pointman. Roku 1968 přidělen k Company F (LRP) 58th Infantry. Roku 1969 převelen k Company D/75th Infantry Ranger.<br>
<br> Současné přidělení u k Company D/75th Infantry Ranger - 2nd Field Force Vietnam.<br>

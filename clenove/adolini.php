<div class=treti>
Narozen 14. 3. 1946 v Palermu, Sicílie.<br>
Roku 1948 spolu s rodiči opustil Sicílii a jako jeden <br>z mnoha ilegálních uprchlíků našel útočiště v Chicagu, Illinois.<br>
Vstoupil do služby v Chicagu, Illinois.<br>
Převelen k 4. pěší divizi a poslán k výcviku do Fort Hood roku 1963. Za kázeňský přestupek  byl vojenským tribunálem odsouzen k odnětí svobody po dobu 24 měsíců. Roku 1967 propuštěn a opět zařazen do výcviku v Fort Campbell. Na vlastní žádost vstoupil do Airborne RECONDO School v Fort Campbell. Roku 1968 převelen do Vietnamu jako příslušník Company F (LRP). 58th Infantry. Roku 1969 převelen k Company D/75th Infantry Ranger.<br>
<br>Současné přidělení u k Company D/75th Infantry Ranger - 2nd Field Force Vietnam

<b>Medaile</b><br>
Jsou udelovány na základe návrhu velitele, nebo vetšiny jednotky (bez dotycného, kterému má být medaile udelena) a potvrzení od velícího. Ciny za které jsou udelovány jsou individuální. Medaile jsou predávány slavnostne pred nastoupenou jednotkou a potvrzeny dokumentem.
Pokud se jedná o medaili za bojovou akci, je nutné, aby dotycný tuto akci prežil (a to celou) jinak nemá nárok na udelení vyznamenání.
